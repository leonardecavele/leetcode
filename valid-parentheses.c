bool	isValid(char *s)
{
	int		i = -1;
	char	stack[2048];

	while (*s)
	{
		if (*s == '[' || *s == '(' || *s == '{')
			stack[++i] = *s;
		else if ((*s == ']' && (i < 0 || stack[i] != '['))
			|| (*s == ')' && (i < 0 || stack[i] != '('))
			|| (*s == '}' && (i < 0 || stack[i] != '{')))
			return (0);
		if (*s == ']' || *s == ')' || *s == '}')
			i--;
		s++;
	}
	return (i == -1);
}
